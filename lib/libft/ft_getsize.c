#include "libft.h"

size_t	ft_getsize(long nbr)
{
	size_t	size;

	size = 0;
	if (nbr <= 0)
		size++;
	while (nbr != 0)
	{
		nbr = nbr / 10;
		size++;
	}
	return (size);
}
